/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  uint8_t procFlag = 0;

  uint8_t pcmOutOffset = 0;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();
  MX_CRC_Init();
  MX_I2S2_Init();
  MX_PDM2PCM_Init();
  /* USER CODE BEGIN 2 */

  //Receive Init
  HAL_I2S_Receive_DMA (&hi2s2, gI2SData, BUFFER_SIZE);


  //Screen Init
  if (ssd1306_Init (&hi2c1) != 0) { //Setup Screen
	Error_Handler ();
  }

  HAL_Delay (500);
  ssd1306_Fill (Black);
  ssd1306_SetCursor (SCREEN_WIDTH/2 - (11 * 2.5), SCREEN_HEIGHT/2 - 18);
  ssd1306_WriteString ("Hello", Font_11x18, White);
  ssd1306_UpdateScreen (&hi2c1);
  HAL_Delay (500);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	if (gDataFlag)
	{
	  uint8_t offset = (gDataFlag - 1) * DATA_SIZE;
	  for (int i = 0; i < DATA_SIZE; i++)
	  {
	  	gPdmBuffer[i] = gI2SData [i + offset]; //take snapshot of data
	  }
	  gDataFlag = 0;
	  procFlag = 1;
	}

	if (procFlag)
	{
      PDM_Filter (gPdmBuffer, gPcmBuffer, &PDM1_filter_handler);

      for (int i = 0; i < SCREEN_WIDTH - DATA_SIZE; i++)
      {
        gDispBuffer[i] = gDispBuffer[i + DATA_SIZE]; //Shift buffer
      }

      for (int i = 0; i < DATA_SIZE ; i++)
      {
        gDispBuffer[i + SCREEN_WIDTH - DATA_SIZE] = gPcmBuffer [i];  //Write to end of buffer
      }

      ssd1306_Fill (Black);
      for (int i = 0; i < SCREEN_WIDTH; i++)
      {
        double scaled = (1.0 * gDispBuffer [i] ) / (1.0 * INT32_MAX); //Scale data between -1 -> 1
        int y = scaled * (SCREEN_HEIGHT / 2.0); //Scale data to screen
        y += (SCREEN_HEIGHT / 2.0); //center y axis

        //Bound y to screen
        y = y > SCREEN_HEIGHT ? SCREEN_HEIGHT : y;
        y = y < 0 ? 0 : y;

        ssd1306_DrawPixel (i, y, White);
      }
      ssd1306_UpdateScreen (&hi2c1);

      pcmOutOffset += DATA_SIZE;
      pcmOutOffset = pcmOutOffset == 2 * DATA_SIZE ? 0 : pcmOutOffset;
	  procFlag = 0;
	}

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

